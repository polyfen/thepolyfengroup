    <h3 id="transparency" class="heading-2">3.1. Transparency</h3>
    <div class="preview">
        <h4 class="heading-3">Transparency is in our DNA</h4>
    </div>
    <p>Transparency is always first within our team, with our clients, and towards the industry.</p>
    <p>We value speaking with candor. We seek feedback from our team and clients actively and continuously. If we want to grow, sometimes hard truths need to be addressed.</p>
    <p>Give progress updates routinely. If you're waiting on a pending answer, follow-up. When in doubt, it's better to over-communicate than to under-communicate.</p>
    <p>Outwardly, this Business Plan and <a data-before="plfn" href="/wiki">Team Wiki</a> are just some of our documentation that is publically available. We also contribute to the open-source software community with public code repositories in <a data-before="github"
        href="https://github.com/polyfen" rel="nofollow" target="_blank">Github</a>.</p>
    <p>We believe that the world of business could learn a lot from the practices and culture of the open-source community.</p>
    <p>Sharing our work allows us to:</p>
    <ul class="hanging-list arrow-bullet spaced-list">
        <li>Document, standardize, and optimize our workflows.</li>
        <li>Give our team an accessible framework to provide feedback, share ideas, and have more participation in the direction of our team culture.</li>
        <li>Gain the interest of potential clients and talent.</li>
        <li>Make accessible educational resources for anyone looking to grow their professional skills and toolset.</li>
        <li>Reach more people through content marketing.</li>
    </ul>
    <hr class="small-separator">
    <h3 id="collaboration" class="heading-2">3.2. Collaboration</h3>
    <div class="preview">
        <h4 class="heading-3">Horizontal communication, fast collaboration</h4>
    </div>
    <p>Together we can achieve much more than we ever could alone. We are heavily commited to providing our team with
        all the tools, resources, and support to maintain a thriving work environment.</p>
    <p>We promote a horizontal team. Doesnt matter if you've just joined the team in a junior position, or if you're a seasoned veteran, don't hesitate to reach out to whoever can facilitate your work. Our team leaders are just a Slack message away and love to hear from you.</p>
    <hr class="small-separator">
    <h3 id="exploration" class="heading-2">3.3. Exploration</h3>
    <div class="preview">
        <h4 class="heading-3">Excellence through R&amp;D</h4>
    </div>
    <p>As a forward-thinking company we invest roughly half of our resources into R&D projects. We implement the latest tech in digital products built by us, for us.</p>
    <p>Our clients count with the advantage of our leading-edge knowhow to beat the competition and never be left behind in an always moving market.</p>
    <hr class="small-separator">
    <h3 id="flexibility" class="heading-2">3.4. Flexibility</h3>
    <div class="preview">
        <h4 class="heading-3">Fully-remote since 2018</h4>
    </div>
    <p>Gumroad's founder, Sahil Lavingia put it in better words that we could when he said "We can't compete with the comp packages that big tech companies can provide. But we can compete—and win—on flexibility."</p>
