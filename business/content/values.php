    <h3 id="transparency" class="heading-2">3.4. Transparency</h3>
    <div class="preview">
        <h4 class="heading-3">Transparency is in our DNA</h4>
    </div>
    <p>Our Business Plan and <a data-before="plfn" href="/wiki">Team Wiki</a> are just some of our documentation that is publically
        available. We also contribute to the open-source software community with public code repositories in <a data-before="github"
        href="https://github.com/polyfen" rel="nofollow" target="_blank">Github</a>. We believe that the world of
        business could learn a lot from the practices and culture of the open-source community. Sharing our work allows
        us to:</p>
    <ul class="hanging-list arrow-bullet spaced-list">
        <li>Reach more people with through content marketing.</li>
        <li>Make accessible educational resources for free to students starting out in a new career as well as
            well-stablished professionals looking to grow.</li>
        <li>Gain the interest of potential clients and talent.</li>
        <li>Document, standardize, and optimize our workflows.</li>
        <li>Give our team-members an accessible framework to provide feedback, share ideas, and have more participation
            in the direction of our team culture.</li>
    </ul>
    <p>We don't perceive the idea of anyone "copying" us as a threat, because we value the innovative potential of our
        team miles ahead of any individual idea that we might've had and shared.</p>
    <p>Wired's documentary of Shenzen, China, the hardware capital of the world shines a light on the philosophy that if
        somebody tries to copy your work and ends up doing better, it's because your results where lacking and the
        market recognized the value of a more capable competitor.</p>
