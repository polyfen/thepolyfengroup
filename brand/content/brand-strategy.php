<h3 id="naming" class="heading-2">3.4. Naming</h3>
<h4 id="etymology" class="heading-3">Etymology</h4>
<p>"Polyfen" is an abstraction of the term “Polyphenic trait”. A genetic property that allows multiple observable variations on organisms of a single species.</p>
<p>The different color patterns on the shells of mollusks are a polyphenic trait.</p>
<img src="imgs/shell-patterns.jpeg" alt="">
