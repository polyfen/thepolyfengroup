<h3 id="creative-concept" class="heading-2">3.2. Creative Concept</h3>
<div class="preview">
    <h4 class="heading-3 align-center" style="max-width:500px;margin:0 auto;padding-top:0;">Where design &amp; development click</h4>
</div>
<p>We distilled the escense of the business into a short, poignant phrase that represents what we stand for, what sets us apart.</p>
<p>The purpose of a Creative Concept is to guide the branding process with a unifying message that ties together every interaction with the audience. It should be represented in the look-and-feel of the logo, typography selection, color scheme, website UI/UX, down to the copywriting for an ad or social media post.</p>
