<h2 class="heading-1">2. Sprints</h2>
<p>We organize our tasks in <strong>short work cycles with clear goals</strong> called “Sprints”.</p>
<p>Depending on the project, the sprint can be one or two weeks long.</p>
<p>We begin each sprint with a planning meeting with this agenda:</p>
<ol class="hanging-list">
    <li><strong>General announcements</strong> concerning the project and team.</li>
    <li><strong>Progress review</strong> of the work done since previous Tuesday.</li>
    <li><strong>Retrospective:</strong></li>
    <ol class="hanging-list" style="list-style-type:upper-alpha;">
        <li>What did we do well?</li>
        <li>What could we have been done better?</li>
    </ol>
<li><strong>Assigning tasks</strong> that have to be completed in the new sprint.</li>
</ol>
</section>