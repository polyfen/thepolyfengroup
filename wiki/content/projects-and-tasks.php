    <p>There are 5 stages in the workflow of any task.</p>
    <h3 class="heading-2" id="backlog">5.1. Backlog</h3>
    <p>We identify a requirement and add it to Asana as a task following SMART task practices:</p>
    <ul class="hanging-list arrow-bullet">
        <li><strong>Specific</strong></li>
        <li><strong>Measurable</strong></li>
        <li><strong>Achievable</strong></li>
        <li><strong>Relevant</strong></li>
        <li><strong>Time-bound</strong></li>
    </ul>
    <h3 class="heading-2" id="to-do">5.2. To Do</h3>
    <p>All tasks that belong to the present <a href="#sprints">Sprint</a>.</p>
    <h3 class="heading-2" id="in-progress">5.3. In Progress</h3>
    <p>One you start work on a specific task, the first thing you must do is set its status as "In Progress" to let the rest of the team know what's being worked on.</p>
    <h3 class="heading-2" id="for-review">5.4. For Review</h3>
    <p>After you consider your task is complete, then change the task status to "For Review" and assign the task to your reporter (who would most likely be Boris).</p>
    <h3 class="heading-2" id="complete">5.5. Complete</h3>
    <p>After your task is reviewed by a Reporter and approved it will be moved to the "Complete" column.</p>
