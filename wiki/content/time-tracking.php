    <h2 class="heading-1">6. Time Tracking</h2>
    <p>Work tracked in Clockify should have the same name as the corresponding Asana task.</p>
    <p>If there is no related Asana task, feel free to name the timelog at your will.</p>
    <p>However, there are two things that must be carefully set:</p>
    <ol class="hanging-list">
        <li><strong>Clockify Project</strong></li>
        <li><strong>Billability</strong></li>
    </ol>
    <p>The Clockify Project should have the same name as the Asana project, which follows the convention "[Client Code] â€º [Project Type]".</p>
    <p>Billability should be set to "Billable" for all work corresponding to Clients of Polyfen and not internal projects.</p>
